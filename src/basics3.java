// Arithmetic operations on double
public class basics3 {
    public static void main(String[] args) {
        double num1 = 8;
        double num2 = 5;
        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        System.out.println(num2 / num1);
    }
}